                    <h2 class="post-title mt-3">D4 Keamanan Sistem Informasi</h2>
                </div>
                <div class="post-content mt-4">
                    <img class="size-full wp-image-2448555 aligncenter" src="https://ti.polbeng.ac.id/images/profil/njisdipqn340Gedung Rektorat dan TI.jpg" alt="Visi dan Misi" width="680" height="381">
                    <p style="text-align: justify;"></p>
                    <p><strong>Visi :</strong></p>

                    <p style="text-align:justify;">Visi program studi Sarjana Terapan Keamanan Sistem Informasi adalah Menjadi Program Studi Sarjana Terapan yang Unggul dalam bidang Keamanan Informasi.</p>

                    <p><strong><em>Penjelasan visi:</em></strong></p>

                    <p style="text-align:justify; text-indent: 45px;">Program studi ini yang menawarkan program pendidikan tingkat sarjana yang fokus pada penerapan praktis dalam bidang keamanan informasi. Ini mencerminkan pendekatan yang kuat terhadap pemahaman teori dan keterampilan praktis yang relevan dengan kebutuhan industri.</p>

                    <p style="text-align:justify; text-indent: 45px;">Kata Unggul Mengacu pada standar keunggulan akademik dan kualitas program studi dalam memberikan pendidikan dan pelatihan di bidang keamanan informasi. Program ini bertujuan untuk menjadi yang terdepan dan diakui secara luas dalam lingkup Provinsi Riau.</p>

                    <p style="text-align:justify; text-indent: 45px;">Bidang keilmuan Keamanan Informasi merujuk pada spesialisasi dalam keamanan sistem informasi, perlindungan data, dan mitigasi risiko terkait keamanan informasi. Program studi ini akan mengajarkan pengetahuan dan keterampilan yang diperlukan untuk melindungi informasi sensitif dan menghadapi ancaman keamanan siber.</p>

                    <p><strong>Misi :</strong></p>

                    <p style="text-align:justify;">Untuk dapat merealisasikan Visi yang telah ditetapkan tersebut, Prodi Sarjana Terapan KSI menetapkan Misi, sebagai berikut:</p>

                    <ol>
                        <li style="text-align:justify;">Menyelenggarakan pendidikan vokasi berkualitas yang berfokus dibidang keamanan informasi, dengan kurikulum yang komprehensif dan relevan, untuk mempersiapkan mahasiswa yang professional dan berkualitas untuk menghadapi tantangan global</li>
                        <li style="text-align:justify;">Membangun budaya akademik dan organisasi yang berkarakter dan bermartabat dengan menjunjung tinggi etika professional kepada Mahasiswa</li>
                        <li style="text-align:justify;">Melakukan&nbsp; penelitian terapan dan inovasi dibidang keamanan informasi untuk menghasilkan kontribusi dalam pemecahan masalah keamanan informasi sesuai dengan kebutuhan masyarakat dan industry.</li>
                        <li style="text-align:justify;">Mengembangkan kemitraan dengan industri dan lembaga terkait untuk memperkaya pengalaman mahasiswa dan mendorong adopsi solusi keamanan yang efektif.</li>
                        <li style="text-align:justify;">Melakukan program pengabdian pada masyrakat untuk menghasilkan karya yang dapat diaplikasikan untuk menyelesaikan permasalahan yang terjadi di masyarakat dan industry.</li>
                    </ol>

                    <p><strong>Tujuan :</strong></p>

                    <p style="text-align:justify;">Dalam rangka untuk mewujudkan visi dan melaksanakan misi Prodi sarjana terapan KSI, maka tujuan yang harus dicapai adalah:</p>

                    <ol>
                        <li style="text-align:justify;">Menghasilkan lulusan berkualitas yang memiliki pengetahuan dan keterampilan unggul dalam bidang keamanan informasi, siap untuk menghadapi tantangan dan tuntutan industri di lingkungan global.</li>
                        <li style="text-align:justify;">Membangun budaya akademik yang berintegritas dan berkarakter, serta menerapkan etika professional dalam semua aspek kehidupan akademik dan organisasi, terutama dalam interaksi dengan mahasiswa.</li>
                        <li style="text-align:justify;">Mendorong penelitian terapan dan inovasi dengan fokus pada pemecahan masalah keamanan informasi yang relevan dengan kebutuhan industri dan masyarakat.</li>
                        <li style="text-align:justify;">Menjalin kemitraan yang erat dengan industri dan lembaga terkait untuk memberikan pengalaman praktis dan kesempatan kolaborasi dalam pengembangan solusi keamanan informasi yang efektif.</li>
                        <li style="text-align:justify;">Melakukan program pengabdian kepada masyarakat dalam penerapan praktik keamanan informasi untuk meningkatkan kesadaran dan keamanan dalam komunitas dan industri.</li>
                    </ol>

                    <p><strong>Sasaran :</strong></p>

                    <p style="text-align:justify;">Sasaran yang hendak dicapai oleh Program Studi dalam rangka mewujudkan visi dan tujuan adalah sebagai berikut:</p>

                    <p style="text-align:justify;">1. Sasaran dari tujuan menghasilkan lulusan berkualitas yang memiliki pengetahuan dan keterampilan unggul dalam bidang keamanan informasi, siap untuk menghadapi tantangan dan tuntutan industri dan lingkungan global yaitu:</p>

                    <ul>
                        <li style="text-align:justify;">Meningkatkan mutu dan relevansi kurikulum keamanan informasi untuk mencerminkan perkembangan terkini dalam teknologi dan tuntutan industri.</li>
                        <li style="text-align:justify;">Memperkuat pelatihan keterampilan praktis dan penerapan pengetahuan dalam situasi dunia nyata melalui magang dan proyek-proyek keamanan informasi.</li>
                        <li style="text-align:justify;">Menyediakan kesempatan bagi mahasiswa untuk mengembangkan kompetensi bahasa asing dan soft skills yang dibutuhkan dalam lingkungan kerja global.</li>
                        <li style="text-align:justify;">Melakukan evaluasi dan perbaikan secara berkala untuk memastikan lulusan dapat memenuhi kebutuhan industri dan tuntutan lingkungan kerja global.</li>
                    </ul>

                    <p style="text-align:justify;">2. Sasaran dari tujuan mendorong penelitian terapan dan inovasi yang berfokus pada pemecahan masalah keamanan informasi yang relevan dengan kebutuhan industri dan masyarakat yaitu:</p>

                    <ul>
                        <li style="text-align:justify;">Memberikan dukungan bagi dosen dan mahasiswa untuk mengadakan penelitian terapan yang berfokus pada keamanan informasi dan menyelesaikan masalah nyata dalam industri.</li>
                        <li style="text-align:justify;">Membangun kolaborasi antara dosen, mahasiswa, dan industri untuk mengidentifikasi isu-isu keamanan informasi yang mendesak dan mendukung penyelesaian masalah keamanan yang relevan dengan kebutuhan industri.</li>
                        <li style="text-align:justify;">Mendorong presentasi dan publikasi hasil penelitian guna memperluas pemahaman dan kontribusi pengetahuan dalam bidang keamanan informasi.</li>
                    </ul>

                    <p style="text-align:justify;">3. Sasaran dari tujuan membangun budaya akademik yang berintegritas dan berkarakter, serta menerapkan etika professional dalam semua aspek kehidupan akademik dan organisasi, terutama dalam interaksi dengan mahasiswa yaitu:</p>

                    <ul>
                        <li style="text-align:justify;">Menerapkan kode etik yang jelas dan standar integritas di antara dosen, mahasiswa, dan staf untuk menciptakan lingkungan akademik yang bermartabat dan adil.</li>
                        <li style="text-align:justify;">Menyediakan pelatihan dan program pengembangan diri bagi mahasiswa dan dosen dalam hal etika professional dan interaksi yang bermartabat.</li>
                        <li style="text-align:justify;">Membangun dan memelihara sikap saling menghormati dan kerjasama yang positif antara dosen dan mahasiswa, dan mendorong dialog terbuka tentang masalah etika dan akademik.</li>
                    </ul>

                    <p style="text-align:justify;">4. Sasaran dari tujuan menjalin kemitraan yang erat dengan industri dan lembaga terkait untuk memberikan pengalaman praktis dan kesempatan kolaborasi dalam pengembangan solusi keamanan informasi yang efektif yaitu:</p>

                    <ul>
                        <li style="text-align:justify;">Membangun jaringan dan kerjasama yang kuat dengan perusahaan dan organisasi terkait di bidang keamanan informasi melalui acara, seminar, dan pertemuan industri.</li>
                        <li style="text-align:justify;">Menyediakan kesempatan magang, kunjungan lapangan, dan kolaborasi dalam proyek-proyek keamanan informasi dengan pihak industri untuk memperkaya pengalaman belajar mahasiswa.</li>
                        <li style="text-align:justify;">Mendorong pertukaran pengetahuan dan pengalaman antara dosen dan profesional industri untuk mengidentifikasi kebutuhan keamanan informasi.</li>
                    </ul>

                    <p style="text-align:justify;">5. Sasaran dari tujuan melakukan program pengabdian kepada masyarakat dalam penerapan praktik keamanan informasi untuk meningkatkan kesadaran dan keamanan dalam komunitas dan industry yaitu:</p>

                    <ul>
                        <li style="text-align:justify;">Melakukan program pelatihan dan seminar tentang keamanan informasi untuk masyarakat umum dan industri guna meningkatkan kesadaran tentang ancaman keamanan dan praktik yang tepat.</li>
                        <li style="text-align:justify;">Menyediakan layanan dan dukungan keamanan informasi bagi masyarakat dan organisasi untuk membantu mereka dalam meningkatkan keamanan dan mengatasi masalah keamanan.</li>
                    </ul>

                    <p><strong>Profil Lulusan :</strong></p>

                    <ol>
                        <li style="text-align:justify;">Lulusan memiliki kemampuan mengembangkan rencana dan kebijakan keamanan informasi serta memiliki kemampuan menguji, analisis dan menilai potensi risiko keamanan informasi pada sistem informasi beserta perangkat pendukungnya untuk memastikan kerahasiaan, integritas, dan ketersediaan informasi yang dimiliki oleh organisasi.</li>
                        <li style="text-align:justify;">Lulusan memiliki kemampuan menganalisis, merancang, membangun dan menguji dan mengimplementasikan sistem informasi sesuai dengan tujuan organisasi</li>
                        <li style="text-align:justify;">Lulusan memiliki kemampuan merancang jaringan komputer, mengimplementasikan, mengelola dan mengujinya sesuai dengan prinsip keamanan informasi untuk melindungan aset informasi yang dimiliki oleh organisasi</li>
                        <li style="text-align:justify;">Lulusan memiliki kemampuan etika profesionalitas, integritas, jujur dan bertanggung jawab terhadap peran yang diemban</li>
                        <li style="text-align:justify;">Lulusan memiliki kemampuan kerja tim, kepemimpinan, berfikir kritis, komunikasi, dan kolaborasi serta jiwa enterpreneurship</li>
                    </ol>

